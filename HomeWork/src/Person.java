public class Person {
    int id;
    int age;
    String name;
    String surname;
    int experince ;

    public Person(int id , int age , String name , String surname , int experince){
        this.id=id;
        this.age=age;
        this.name=name;
        this.surname=surname;
        this.experince =experince;
    }
}
